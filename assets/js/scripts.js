function removeFromCart(id) {
    console.log(id)
    event.preventDefault()
    let item = items.array
    let j = cart.indexOf(id)
    cart.splice(j, 1)
    $('#item' + item[id].id + '').remove()
}

function cartDisplay() {
    let item = items.array;
    let prices = []
    let total = 0
    if (cart.length != 0) {
        for (let i = 0; i < cart.length; i++) {
            $('.cart-items').append(
                '<li id="item' + item[cart[i]].id + '">' +
                '                <div class="collapsible-header cart-item-title">' + item[cart[i]].name + '<span' +
                '                        class="black-text price-collapsible">' + item[cart[i]].price + '€</span></div>' +
                '                <div class="collapsible-body cart-item-body"><img src="assets/img/' + item[cart[i]].file + '"' +
                '                        class="responsive-img" alt="' + item[cart[i]].alt + '">' +
                '                    <p>' + item[cart[i]].infos + '</p>' +
                '                    <button class="btn waves-effect waves-light red" onclick="removeFromCart(' + item[cart[i]].id + ')" type="submit" name="action">Supprimer' +
                '                        <i class="material-icons right">clear</i>' +
                '                    </button>' +
                '                </div>' +
                '            </li>'
            )
            prices.push(item[cart[i]].price)
        }
        if (prices.length != 0) {
            for (let j = 0; j < prices.length; j++) {
                total = total + prices[j]
            }
        }
        $('#price').text(total)
    } else {
        $('.cart-items').append(
            '<p>Le panier est vide, pensez à le remplir !</p>'
        )
    }
}

function filterWeapons() {
    let min = $('#w-min').val()
    let max = $('#w-max').val()
    let type = $('#w-type').val()
    let brand = $('#w-brand').val()
    let caliber = $('#w-caliber').val()
    let filters = [];

    $('.item-weapons-row').empty()

    if (!min || !Number(min)) {
        min = 0;
    }
    if (!max || !Number(max)) {
        max = 0
    }
    if (min > max) {
        let temp = max
        max = min
        min = temp
    }
    if (type != null) {
        filters.push(type)
    } else if (!type || type == 0) {

        type = 0
        filters.push(type)
    }
    if (brand != null) {
        filters.push(brand)
    } else if (!brand || brand == 0) {

        brand = 0
        filters.push(brand)
    }
    if (caliber != null) {
        filters.push(caliber)
    } else if (!caliber || caliber == 0) {

        caliber = 0
        filters.push(caliber)
    }
    if (max != null) {
        filters.push(max)
    } else if (!max || max == 0) {
        max = 0
        filters.push(max)
    }
    if (min != null) {
        filters.push(min)
    } else if (!min || min == 0) {
        min = 0
        filters.push(min)
    }
    createWeapons(filters)
    filters = []
}

